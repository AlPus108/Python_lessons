# Массив или СПИСОК (в Питоне)
# Используются для хранения наборов значений. Элементами которых могут быть произовльные значения:
# строки, числа, произвольные объекты, другие списки.
# В рамках одного сиска можно кобинировать объекты разных типов.

students = ['Ivan', 'Masha', 'Sasha']
for student in students:
    print('Hello ' + student + '!')


